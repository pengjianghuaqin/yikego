public class LocationHistoryList {

    private String streetName;
    private String streetLongitude;
    private String streetLatitude;
    
    public String getStreetName() {
        return streetName;
    }

    public void setStreetName(String streetName) {
        this.streetName = streetName;
    }
    
    public String getStreetLongitude() {
        return streetLongitude;
    }

    public void setStreetLongitude(String streetLongitude) {
        this.streetLongitude = streetLongitude;
    }
    
    public String getStreetLatitude() {
        return streetLatitude;
    }

    public void setStreetLatitude(String streetLatitude) {
        this.streetLatitude = streetLatitude;
    }
}
